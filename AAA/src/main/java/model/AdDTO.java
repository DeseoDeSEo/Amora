package model;

public class AdDTO {
	

	private int ad_Num;
	private String ad_Title;
	private String ad_Content;
	private String id;
	private String nickname;
	private String ad_Date;
	private String ad_Period;
	private int ad_Views;
	private String role;
	
	
	public int getAd_Num() {
		return ad_Num;
	}
	public void setAd_Num(int ad_Num) {
		this.ad_Num = ad_Num;
	}
	public String getAd_Title() {
		return ad_Title;
	}
	public void setAd_Title(String ad_Title) {
		this.ad_Title = ad_Title;
	}
	public String getAd_Content() {
		return ad_Content;
	}
	public void setAd_Content(String ad_Content) {
		this.ad_Content = ad_Content;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getAd_Date() {
		return ad_Date;
	}
	public void setAd_Date(String ad_Date) {
		this.ad_Date = ad_Date;
	}
	public String getAd_Period() {
		return ad_Period;
	}
	public void setAd_Period(String ad_Period) {
		this.ad_Period = ad_Period;
	}
	public int getAd_Views() {
		return ad_Views;
	}
	public void setAd_Views(int ad_Views) {
		this.ad_Views = ad_Views;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}

	

}   